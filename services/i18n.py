def t(key: str) -> str:
    """Retourne le texte traduit selon la langue actuelle."""
    return MESSAGES.get(CURRENT_LANG, MESSAGES["en"]).get(key, key)


def toggle_language():
    """Bascule entre l'anglais et le français."""
    global CURRENT_LANG
    CURRENT_LANG = "fr" if CURRENT_LANG == "en" else "en"